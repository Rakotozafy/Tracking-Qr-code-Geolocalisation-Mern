import mongoose from 'mongoose';

const authSchema = mongoose.Schema({
    title: String,
    message: String,
    creator: String,
    tags: [String],
    selectedFile: String,
    likeCount: {
        type: Number,
        default: 0,
    },
    createdAt: {
        type: Date,
        default: new Date(),
    },
})

var AuthMessage = mongoose.model('AuthMessage', authSchema);

export default AuthMessage;